
package softwareamedidad.com.matematicas;


public class Calculos {
    
    public static double resultado;
    
    public static double suma(int a,int b){
        
        resultado=a+b;
        
        return resultado;
        
    }
    
     public static double resta(int a,int b){
        
        resultado=a-b;
        
        return resultado;
        
    }
     
    public static double mutiplica(int a,int b){
        
        resultado=a*b;
        
        return resultado;
        
    }
    
}
